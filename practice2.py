# 연산자
print(1+1)
print(3-2)
print(5*2)
print(6/3)

print(2**3) # 2의 세 제곱
print(5%3)
print(5//3) #나누기 연산자

print(10>3) # True
print(4 >= 7) # False

print(3 == 3) # True
print(4 == 2) # False
print( 3 + 4 == 7) # True

print(1 != 3) # True
print(2**3 != 8) #False

print(not(1 != 3)) # False

print((2>0) and (2<5)) # True
print((2>0) & (2<5)) # True

print((2 > 0) or (2 > 5)) # True
print((2 > 0) | (2 > 5)) # True

print(5 > 4 > 3) # True
print(5 > 4 > 7) # False

